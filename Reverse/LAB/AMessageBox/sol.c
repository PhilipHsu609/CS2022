uint8_t rol(uint8_t val, size_t count) {
    return (val << count) | (val >> (8 - count));
}

uint8_t ror(uint8_t val, size_t count) {
    return (val >> count) | (val << (8 - count));
}

int main() {
    for(int i = 0; i < 38; i++) {
        printf("%c", ror(flag[i] ^ 0x87, 3));
    }
    printf("\n");
    return 0;
}